public class ConversaoDeUnidadeDeArea {
        public static double conversaoQuadradoPes(double metrosQuadrados){
            double conversao = metrosQuadrados * 10.76;
            return conversao;
        }
    
        
        public static double conversaoPeQuadradoCentimetros(double péQuadrado){
            double conversao = péQuadrado * 929;
            return conversao;
        }
    
        public static double conversaoMilhaQuadradaAcres(double milhaQuadrada){
            double conversao = milhaQuadrada * 640;
            return conversao;
        }
    
        
        public static double conversaoAcrePesQuadrados(double acre){
            double conversao = acre * 43560;
            return conversao;
        }
}
